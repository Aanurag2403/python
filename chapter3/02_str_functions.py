name = "anurag"

#length of string
print(len(name))

#finds ending string
print(name.endswith("rag"))

#finds starting character
print(name.startswith('anu'))

#capitalizes first word
print(name.capitalize())

#counts no. of words
print(name.count("a"))

#finds that specific word 
print(name.find("r"))

#replaces that word with another one
print(name.replace("a","r"))